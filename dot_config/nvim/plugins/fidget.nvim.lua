-- ==============================================================================
-- fidget.nvim 設定
-- LSPの進捗状況を画面右下に通知表示するプラグイン
-- ==============================================================================

require("fidget").setup({
    -- LSP進捗通知の設定
    progress = {
        display = {
            -- 完了後のメッセージ表示時間（秒）
            -- 3秒間表示してから消える
            done_ttl = 3,
        },
    },
    -- 通知ウィンドウの設定
    notification = {
        window = {
            -- 背景の透明度（0 = 完全に不透明）
            -- ターミナルの背景色と合わせるため不透明に設定
            winblend = 0,
        },
    },
})
